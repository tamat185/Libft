#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	long int	znak;
	long int	num;

	znak = 1;
	num = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			znak = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - 48);
		i++;
	}
	if (i > 19 && znak == 1)
		return (-1);
	if (i > 19 && znak == -1)
		return (0);
	return (num * znak);
}
